		   <h1>What to Expect</h1>
		    <h2>Surgery</h2>
           
	         <br>
            On the day of surgery you come in an hour or two early to relax and be prepared. The exact times will be given to you by our booking staff. 
             <p>Please do not wear eye make-up.</p>
             <p>Because we give you a mild sedative you will need to bring a driver.</p>
             <p>Our theatre staff explain what you will need to do every step of the way so you don&rsquo;t even have to think on the day. Just let it happen !</p>
             <p>Basically, you come in and lie down, relax and stare at the little red light which keeps your eye still. The doctor keeps your eye open with a small retainer which feels like you are opening your eyes very wide with your finger tips.</p>
             <p>Remember, millions of people have had this done and it is not hard !</p>
             <p>There is a pressure sensation as the microkeratome begins to make the flap. Your vision may fade at this stage and that is normal. It returns when the flap is complete.</p>
             <p>The laser is then performed and the other eye done. </p>
             <p>All up it takes about 20 minutes for the two eyes.</p>
             <p>You wait for half an hour and then go home with your escort.</p>
             <p>We like you to wear an eye shield overnight to prevent you rubbing your eyes.</p>
             <p>&nbsp;&nbsp;</p>
